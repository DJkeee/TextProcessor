package org.example.text_readers

/**
 * Интерфейс для реализации компонентов чтения данных.
 *
 * Определяет базовый контракт для всех классов, отвечающих за чтение данных из различных источников.
 * Реализации этого интерфейса могут читать данные из файлов, консоли, сетевых соединений,
 * баз данных или других источников ввода.
 *
 * @example
 * ```
 * class NetworkReader : Reader {
 *     override fun read() {
 *         // реализация чтения из сети
 *     }
 * }
 *
 * class DatabaseReader : Reader {
 *     override fun read() {
 *         // реализация чтения из базы данных
 *     }
 * }
 * ```
 */
interface Reader {
    /**
     * Выполняет операцию чтения данных из источника.
     */
    fun read()
}