package org.example.text_savers

import org.example.data_classes.Sentence

/**
 * Интерфейс для сохранения текстовых данных в файл.
 *
 * Определяет контракт для классов, реализующих различные стратегии сохранения
 * текстовой информации. Реализации могут сохранять текст в разных форматах:
 * исходный текст, нормализованный текст, статистику или комбинированные отчеты.
 *
 * @see Sentence класс, хранящий предложения
 */
interface TextSaver {
    /**
     * Сохраняет список предложений в указанный файл.
     *
     * @param text список предложений для сохранения
     * @param path путь к файлу для сохранения. Должен включать имя файла и расширение.
     */
    fun save(text: List<Sentence>, path: String)
}